package de.sofd.draw2d.viewer;


public class NoSuchMouseHandleException extends RuntimeException {

    public NoSuchMouseHandleException() {
        super();
    }

    public NoSuchMouseHandleException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoSuchMouseHandleException(String message) {
        super(message);
    }

    public NoSuchMouseHandleException(Throwable cause) {
        super(cause);
    }

    
}
